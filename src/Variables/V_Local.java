package Variables;

public class V_Local {
    /**
     * las variables de ambito local, o tambien conocidas como variables de bloque, son aquellas que solo pueden
     * ser accedidas desde el bloque de codigo en el que han sido declaradas.( desde metodos).
     *  - se debe inicializar las variables.
     *____________________________________________________________________________________________________
     * las variables de ambito global  o de instancia son aquellas que pertenecen a la clase donde han sido
     * declaradas, y dependiendo del modificador de acceso utlizado, podran ser accedidas unicamente desde
     * la misma clase.
     *  - estan declaradas fuera de cualquier metodo.
     *  - las variables de global no se inicializan.
     *  - para mantener la encapsulacion se utiliza los modificadores de acceso: - private
     *                                                                           - public
     *                                                                           - protec
     *                                                                           - default
     *____________________________________________________________________________________________________
     * variables estaticas: tambien conocidas como variables de clase, son aquellas que pertenecen a la
     * propia clase donde han sido declaradas y para poder acceder a ellas no es necesario crear una instancia
     * de clases.
     */
}
